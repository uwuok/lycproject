    fx, fy = 0.1, 0.1

    # 灰階處理
    g1, g2 = roi1, roi2
    if len(roi1) == 3:
        g1 = cv2.cvtColor(roi1, cv2.COLOR_BGR2GRAY)
    if len(roi2) == 3:
        g2 = cv2.cvtColor(roi2, cv2.COLOR_BGR2GRAY)

    # 掩膜處理
    r1 = mask_roi(g1, ms1)
    r2 = mask_roi(g2, ms2)
    r1 = cv2.resize(r1, None, fx=fx, fy=fy)
    return r1, r2
